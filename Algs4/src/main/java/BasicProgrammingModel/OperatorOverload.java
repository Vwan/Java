public class OperatorOverload {
    public static void main(String[] args){

        System.out.println (5/3);
        System.out.println ((double)(5/3));
        System.out.println (5/3.0);
        System.out.println (5.0/3.0);

        System.out.println ((int)5/(double)3);
    }
}
